export type MessageKey = keyof typeof messages['zh-CN']

let cachedLocale: AppLocale | null = null

export function getPreferredLocale(): AppLocale {
  if (cachedLocale) return cachedLocale

  const timeZone = Intl.DateTimeFormat().resolvedOptions().timeZone
  cachedLocale = CHINA_TIME_ZONES.has(timeZone) ? 'zh-CN' : 'en-US'
  return cachedLocale
}

export function getLocaleTag(locale = getPreferredLocale()) {
  return locale
}

export function applyDocumentLocale() {
  if (typeof document === 'undefined') return
  document.documentElement.lang = getPreferredLocale() === 'zh-CN' ? 'zh-CN' : 'en'
}

export function tForLocale(locale: AppLocale, key: MessageKey, vars?: Record<string, string | number>) {
  let value: string = messages[locale][key]
  if (!vars) return value

  for (const [name, replacement] of Object.entries(vars)) {
    value = value.replaceAll(`{${name}}`, String(replacement))
  }

  return value
}

export function t(key: MessageKey, vars?: Record<string, string | number>) {
  return tForLocale(getPreferredLocale(), key, vars)
}
